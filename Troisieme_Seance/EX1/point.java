package Troisieme_Seance.EX1;

public class point {
    float x;
    float y;
    public point(float x,float y){
        this.x = x;
        this.y = y;
    }
    // Returns a string representation of the object
    public String toString(){
        return "point [x="+ x + ",y=" + y + "]";  
    }
}
